
OLTP: - (Online Transaction Processing): - focused on query processing, maintaining data integrity in multi-access environments 
as well as effectiveness that is measured by the total number of transactions per second. An example of the OLTP system is the ATM center.

OLAP : -stands for Online Analytical Processing,
It is characterized by a large volume of data.

OLAP uses the data warehouse.
It is designed for analysis of business measures by category and attributes.
 